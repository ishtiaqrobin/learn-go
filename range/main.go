package main

import (
	"fmt"
	"time"
)

func main() {
	//* =========================================================================
	// ১. ARRAY (অ্যারে) - স্ট্যাটিক বা ফিক্সড সাইজের ডাটা স্ট্রাকচার
	//* =========================================================================
	fmt.Println("--- 1. Array Demo ---")

	// [ইন্টারনাল]: অ্যারের সাইজ [3] ডিক্লেয়ারেশনের সময়ই ফিক্সড করে দিতে হয়।
	// গো মেমোরিতে ৩টি স্ট্রিং রাখার জন্য একটা টানা (contiguous) জায়গা বুক করে।
	myArr := [3]string{"red", "green", "blue"}

	// 💡 কখন ব্যবহার করবে?
	// -> যখন ডাটার সংখ্যা ১০০% ফিক্সড এবং জানা থাকে।
	// -> উদাহরণ: সপ্তাহের ৭ দিন, বছরের ১২ মাস, বা দাবা বোর্ডের ৮x৮ ঘর।
	// -> পারফরম্যান্সের দিক থেকে এটি সবচেয়ে ফাস্ট কারণ মেমোরি আগে থেকেই ফিক্সড থাকে।

	for i, value := range myArr {
		fmt.Printf("Index: %d, Value: %s\n", i, value)
	}
	fmt.Println()

	//* =========================================================================
	// ২. SLICE (স্লাইস) - ডাইনামিক এবং ফ্লেক্সিবল উইন্ডো
	//* =========================================================================
	fmt.Println("--- 2. Slice Demo ---")

	// [ইন্টারনাল]: থার্ড ব্র্যাকেটে কোনো সাইজ না দিলে সেটি Slice হয়।
	// স্লাইস নিজে ডাটা ধরে রাখে না, সে মেমোরিতে থাকা একটি গোপন (Underlying) অ্যারে-কে পয়েন্ট করে।
	colors := []string{"red", "green", "blue"}

	// append() ফাংশন দিয়ে স্লাইসে যত খুশি ডাটা যোগ করা যায় (ডাইনামিক)।
	// ভেতরের গোপন অ্যারে ভরে গেলে গো নিজে থেকে মেমোরিতে ডাবল সাইজের নতুন অ্যারে বানিয়ে ডাটা কপি করে নেয়।
	colors = append(colors, "yellow", "purple")

	// 💡 কখন ব্যবহার করবে?
	// -> গো-ল্যাং-এ লিস্ট বা সিকোয়েন্সিয়াল ডাটা রাখার জন্য ৯৯% সময় স্লাইস ব্যবহার করা হয়।
	// -> যখন ডাটার সংখ্যা আগে থেকে জানা থাকে না বা রানটাইমে ডাটা কম-বেশি হতে পারে।
	// -> উদাহরণ: ডাটাবেজ থেকে ইউজারদের লিস্ট তুলে আনা, শপিং কার্টের প্রোডাক্ট লিস্ট।

	for i, color := range colors {
		fmt.Printf("Index: %d, Color: %s\n", i, color)
	}
	fmt.Println()

	//* =========================================================================
	// ৩. STRING (স্ট্রিং) - বাইটের রিড-অনলি স্লাইস
	//* =========================================================================
	fmt.Println("--- 3. String Demo ---")

	name := "Next Level"

	// [ইন্টারনাল]: গো-তে স্ট্রিং হলো আসলে বাইটসের একটি রিড-অনলি স্লাইস ([]byte)।
	// প্রতিটি ক্যারেক্টারের ASCII/UTF-8 নিউমেরিক ভ্যালু মেমোরিতে জমা থাকে।
	byteSlice := []byte(name)
	fmt.Printf("String as Byte Slice: %v\n", byteSlice)

	// 💡 কখন ব্যবহার করবে?
	// -> যেকোনো ধরনের টেক্সট ডাটা, নাম, মেসেজ, বা ইউজার ইনপুট হ্যান্ডেল করার জন্য।
	// ⚠️ মনে রাখবে: স্ট্রিং 'Immutable' (অপরিবর্তনশীল)। name[0] = 'Z' সরাসরি করা অসম্ভব।
	// টেক্সট মডিফাই করতে হলে তাকে প্রথমে []byte বা []rune-এ রূপান্তর করতে হয়।

	for i, runeValue := range name {
		fmt.Printf("Index: %d, Character: %c (Rune: %d)\n", i, runeValue, runeValue)
	}
	fmt.Println()

	//* =========================================================================
	// ৪. MAP (ম্যাপ) - আন-অর্ডারড কি-ভ্যালু পেয়ার (Hash Table)
	//* =========================================================================
	fmt.Println("--- 4. Map Demo ---")

	// [ইন্টারনাল]: ম্যাপ ব্যবহার করার আগে make() বা ম্যাপ লিটারেল {} দিয়ে মেমোরি ইনিশিয়ালাইজ করতে হয়।
	// সরাসরি var m map[string]int লিখে ডাটা অ্যাসাইন করলে 'nil map' প্যানিক ইরর আসবে।
	myMap := map[string]string{
		"name":    "Ishtiaq",
		"success": "ok",
		"role":    "Developer",
	}

	// 💡 কখন ব্যবহার করবে?
	// -> যখন ইন্ডেক্স (০, ১, ২) এর বদলে কোনো ইউনিক 'Key' (যেমন ID, Email, Name) দিয়ে দ্রুত ডাটা খুঁজতে হবে।
	// -> জাভাস্ক্রিপ্টের Object বা পাইথনের Dictionary-র অল্টারনেটিভ হিসেবে এটি ব্যবহৃত হয়।
	// ⚠️ মনে রাখবে: ম্যাপের কোনো নির্দিষ্ট অর্ডার বা সিরিয়াল থাকে না। লুপ চালালে প্রতিবার সিরিয়াল ওলটপালট হতে পারে।

	for key, value := range myMap {
		fmt.Printf("Key: %s -> Value: %s\n", key, value)
	}
	fmt.Println()

	//* =========================================================================
	// ৫. CHANNEL (চ্যানেল) - গোরুটিনের মধ্যকার নিরাপদ যোগাযোগ পাইপলাইন
	//* =========================================================================
	fmt.Println("--- 5. Channel Demo ---")

	// [ইন্টারনাল]: মেমোরিতে একটি স্ট্রিং টাইপের ডাটা আদান-প্রদানের পাইপলাইন বা চ্যানেল তৈরি করলাম।
	messages := make(chan string)

	// একটি নতুন 'Goroutine' (background thread) চালু করলাম যা মূল কোডের সমান্তরালে চলবে।
	go func() {
		fmt.Println("[Goroutine] Working in the background, waiting 2 seconds...")
		time.Sleep(2 * time.Second)

		// চ্যানেলের ভেতর ডাটা পাঠানো (Arrow-র দিক চ্যানেলের দিকে)
		messages <- "Hello from Background Goroutine!"
	}()

	fmt.Println("[Main] Waiting for data to arrive from the channel (Blocking)...")

	// [ইন্টারনাল]: চ্যানেল থেকে ডাটা রিসিভ করা (Arrow-র দিক ভ্যারিয়েবলের দিকে)
	// যতক্ষণ না ব্যাকগ্রাউন্ড গোরুটিন ডাটা পাঠাচ্ছে, মূল কোড এই লাইনে লক বা ব্লক হয়ে থাকবে।
	receivedMsg := <-messages

	// 💡 কখন ব্যবহার করবে?
	// -> যখন গো-তে একাধিক ব্যাকগ্রাউন্ড টাস্ক (Goroutine) একসাথে চলে এবং তাদের মধ্যে নিরাপদে ডাটা আদান-প্রদান করতে হয়।
	// -> উদাহরণ: ব্যাকগ্রাউন্ডে একটা বড় ফাইল আপলোড হচ্ছে, আপলোড শেষ হলে মেইন থ্রেডকে নোটিফিকেশন পাঠানো।

	fmt.Printf("[Main] I got the data: %s\n", receivedMsg)
}
