package main

// =========================================================================
// ১. দ্য রুলবুক (ইন্টারফেস) - ব্যাটারির খাঁচার সাইজ
// =========================================================================
// কে কী চায়: এই ইন্টারফেসটি একটি 'চুক্তি'। সে বলে- "আমার কোনো ডাটা লাগবে না,
// কিন্তু আমার স্লটে ঢুকতে হলে তোমাকে অবশ্যই 'pay' নামের মেথডটি জানতে হবে।"
type PaymentMethod interface {
	pay(amount float64)
}

// =========================================================================
// ২. দেয়াল ঘড়ি (সার্ভিস স্ট্রাকট)
// =========================================================================
// কে কী নেয়: PaymentService মাস্টার বক্সটি তার ভেতরে একটি 'method' নেয়,
// যার টাইপ হলো উপরের ওই খালি খাঁচা (PaymentMethod)।
type PaymentService struct {
	method PaymentMethod // ঘড়ির পেছনের খালি ব্যাটারি স্লট
}

// =========================================================================
// ৩. ঘড়ির স্লটে ব্যাটারি পুশ করার কন্টেইনার (Constructor)
// =========================================================================
// ওয়ার্কফ্লো: বাইরে থেকে যেকোনো ব্যাটারি এনে এই ফাংশনে দিলে, সে ঘড়ির স্লটে
// ব্যাটারিটা ফিট করে পুরো ঘড়িটার মেমোরি অ্যাড্রেস (*PaymentService) রিটার্ন করে।
func NewPaymentService(method PaymentMethod) *PaymentService {
	return &PaymentService{
		method: method, // স্লটে ব্যাটারি ঢুকিয়ে দেওয়া হলো (Dependency Injection)
	}
}

// =========================================================================
// ৪. ঘড়ির টিকটিক বাটন (Checkout Method)
// =========================================================================
// ওয়ার্কফ্লো: মেইন ফাংশন থেকে যখন এই বাটনটা পুশ করা হবে, ঘড়ি তার স্লটের দিকে তাকাবে
// এবং স্লটে যে কোম্পানির ব্যাটারিই বসে থাকুক না কেন, তার নিজস্ব 'pay()' মেথড চালু করে দেবে।
func (ps PaymentService) checkout() {
	ps.method.pay(10.00) // স্লটে থাকা ব্যাটারির পাওয়ার অন করা হলো
}
